    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String employeeId = dtm.getValueAt(jTable1.getSelectedRow(), 0).toString();
        try {
            Employee employee = EmployeeController.searchEmployee(employeeId);
            if (employee != null) {
                jLabel23.setText(employee.getAddress());
                jLabel41.setText(employee.getGender());
                jLabel35.setText(employee.getNic());
                jLabel34.setText(employee.getMaritialStatus());
                jLabel39.setText(employee.getMobileNum());
                jLabel38.setText(employee.getLandNum());
                jLabel40.setText(employee.getEmail());
                jLabel33.setText(Double.toString(employee.getBasicSalary()));
            }else{
            }
        } catch (ClassNotFoundException | SQLException ex) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

