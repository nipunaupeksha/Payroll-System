    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        jTable1.setVisible(true);
        if (isConstructorClosed) {
            try {
                String roleId = RoleContoller.getRoleId((String) jComboBox2.getSelectedItem());
                ArrayList<Employee> employeeList = EmployeeController.getAllRoles(roleId);
                dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                for (Employee employee : employeeList) {
                    String departmentName = DepartmentController.getDepartmentName(employee.getDepartmentId());
                    String gradeName = GradeContoller.getGradeName(employee.getGradeId());
                    String roleName = RoleContoller.getRoleName(employee.getRoleId());
                    Object[] rawData = {employee.getEmployeeId(), employee.getFirstName() + " " + employee.getSecondName(), departmentName, gradeName, roleName};
                    dtm.addRow(rawData);
                }
            } catch (SQLException | ClassNotFoundException ex) { 
                
            }
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

